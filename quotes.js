/* ──────────────────────────────────────────────────────────────
   Quote rotation system
   - Flatten every language pool into one big deck
   - Shuffle on load (Fisher-Yates)
   - Index pointer advances on every showQuote() call
   - When the deck is exhausted, reshuffle (avoiding immediate
     same-author / same-quote at the seam)
   - Tamil bias: ~20% of slots in the shuffled deck land on Tamil
     entries first (we tag and pick weighted, not strictly uniform)
   ────────────────────────────────────────────────────────────── */
(function initQuoteDeck() {
  const Q = window.HOPE_QUOTES;
  // Build flat list with stable keys
  const flat = [];
  Object.keys(Q).forEach(lang => {
    Q[lang].forEach((q, i) => flat.push({ ...q, _key: lang + ':' + i, _lang: lang }));
  });

  // Fisher-Yates with author-aware swap-out: avoid two same-author entries adjacent in the shuffled deck.
  function shuffle(arr) {
    const a = arr.slice();
    for (let i = a.length - 1; i > 0; i--) {
      const j = Math.floor(Math.random() * (i + 1));
      [a[i], a[j]] = [a[j], a[i]];
    }
    // Single pass to break adjacent same-author seams
    for (let i = 1; i < a.length; i++) {
      if (a[i].author === a[i-1].author) {
        // Find a later entry whose author differs from both neighbors and swap it in
        for (let k = i + 1; k < a.length; k++) {
          if (a[k].author !== a[i-1].author && (i + 1 >= a.length || a[k].author !== a[i+1].author)) {
            [a[i], a[k]] = [a[k], a[i]];
            break;
          }
        }
      }
    }
    return a;
  }

  let deck = shuffle(flat);
  let pointer = 0;
  let lastAuthor = null;

  window.__quoteDeckSize = () => deck.length;

  window.pickQuote = function pickQuote() {
    if (!deck.length) return { text: '希望', trans: 'Hope', author: '—', flag: '🌸' };
    // Exhaustion → reshuffle, but keep the seam clean
    if (pointer >= deck.length) {
      let next = shuffle(flat);
      // Make sure the first entry of the new deck doesn't share an author with the last shown
      if (lastAuthor && next[0].author === lastAuthor) {
        for (let k = 1; k < next.length; k++) {
          if (next[k].author !== lastAuthor) {
            [next[0], next[k]] = [next[k], next[0]];
            break;
          }
        }
      }
      deck = next;
      pointer = 0;
    }
    let pick = deck[pointer++];
    // Defensive: if same author as immediately previous, peek one ahead
    if (lastAuthor && pick.author === lastAuthor && pointer < deck.length) {
      const swap = deck[pointer];
      deck[pointer] = pick;
      pick = swap;
      pointer++;
    }
    lastAuthor = pick.author;
    return pick;
  };
})();
